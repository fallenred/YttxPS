package com.yttx.yttxps.model;

import java.math.BigDecimal;

public class RouteCCType {

	private String fsRouteno;
	
	private BigDecimal fiDayflag;
	
	private String fsRestype;
	
	private String fsResno;
	
	private String fsName;
	
	private String fsScenicno;
	
	private String fsScenicname;

	public String getFsRouteno() {
		return fsRouteno;
	}

	public void setFsRouteno(String fsRouteno) {
		this.fsRouteno = fsRouteno;
	}

	public BigDecimal getFiDayflag() {
		return fiDayflag;
	}

	public void setFiDayflag(BigDecimal fiDayflag) {
		this.fiDayflag = fiDayflag;
	}

	public String getFsRestype() {
		return fsRestype;
	}

	public void setFsRestype(String fsRestype) {
		this.fsRestype = fsRestype;
	}

	public String getFsResno() {
		return fsResno;
	}

	public void setFsResno(String fsResno) {
		this.fsResno = fsResno;
	}

	public String getFsName() {
		return fsName;
	}

	public void setFsName(String fsName) {
		this.fsName = fsName;
	}

	public String getFsScenicno() {
		return fsScenicno;
	}

	public void setFsScenicno(String fsScenicno) {
		this.fsScenicno = fsScenicno;
	}

	public String getFsScenicname() {
		return fsScenicname;
	}

	public void setFsScenicname(String fsScenicname) {
		this.fsScenicname = fsScenicname;
	}
}
